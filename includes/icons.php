/**
 * Display service icon with optional wrapper
 */
function display_service_icon($icon_name, $classes = '', $wrapper_tag = '', $wrapper_classes = '') {
    $svg = get_service_icon_svg($icon_name, $classes);
    
    if (!empty($wrapper_tag)) {
        return sprintf('<%s class="%s">%s</%s>', 
            $wrapper_tag, 
            esc_attr($wrapper_classes), 
            $svg, 
            $wrapper_tag
        );
    }
    
    return $svg;
}

/**
 * Get icon choices for use in WordPress customizer or admin
 */
function get_service_icon_choices() {
    $options = get_service_icon_options();
    $choices = array();
    
    foreach ($options as $key => $label) {
        $choices[$key] = $label;
    }
    
    return $choices;
}


/**
 * Helper function to get Font Awesome icon classes for social media
 */
function get_social_icon_class($platform) {
    $icons = [
        'facebook' => 'fab fa-facebook-f',
        'twitter' => 'fab fa-twitter',
        'youtube' => 'fab fa-youtube',
        'instagram' => 'fab fa-instagram',
        'linkedin' => 'fab fa-linkedin-in',
        'pinterest' => 'fab fa-pinterest-p'
    ];
    
    return $icons[strtolower($platform)] ?? 'fas fa-share-alt';
}
